namespace Helpers
{
    public static class MortonUtil
    {
        public static int GetMorton(int x, int y, int z)
        {
            x = (x & 1) | ((x & 2) << 2) | ((x & 4) << 4) | ((x & 8) << 6) | ((x & 16) << 8) | ((x & 32) << 10) | ((x & 64) << 12) | ((x & 128) << 14) | ((x & 256) << 16) | ((x & 512) << 18);
            y = (y & 1) | ((y & 2) << 2) | ((y & 4) << 4) | ((y & 8) << 6) | ((y & 16) << 8) | ((y & 32) << 10) | ((y & 64) << 12) | ((y & 128) << 14) | ((y & 256) << 16) | ((y & 512) << 18);
            z = (z & 1) | ((z & 2) << 2) | ((z & 4) << 4) | ((z & 8) << 6) | ((z & 16) << 8) | ((z & 32) << 10) | ((z & 64) << 12) | ((z & 128) << 14) | ((z & 256) << 16) | ((z & 512) << 18);
            return (x << 2) | (y << 1) | z;
        }
    }
}